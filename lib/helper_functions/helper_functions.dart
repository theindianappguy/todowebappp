
class HelperFunctions{

  static String getWeek(int dayOfTheWeek){

    switch(dayOfTheWeek){
      case 1:
        return "Monday";
        break;
      case 2:
        return "Tuesday";
        break;
      case 3:
        return "Wednesday";
        break;
      case 4:
        return "Thursday";
        break;
      case 5:
        return "Friday";
        break;
      case 6:
        return "Saturday";
        break;
      case 7:
        return "Sunday";
        break;

    }
  }

  static String getYear(int monthOfTheYear){

    switch(monthOfTheYear){
      case 1:
        return "Jan";
        break;
      case 2:
        return "Feb";
        break;
      case 3:
        return "Mar";
        break;
      case 4:
        return "Apr";
        break;
      case 5:
        return "May";
        break;
      case 6:
        return "June";
        break;
      case 7:
        return "July";
        break;
      case 7:
        return "Aug";
        break;
      case 7:
        return "Sep";
        break;
      case 7:
        return "Oct";
        break;
      case 7:
        return "Nov";
        break;
      case 7:
        return "Dec";
        break;

    }
  }


}